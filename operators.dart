void main(List<String> args) {
  // list all operators with examples
  // 1. Arithmetic Operators
  // 2. Equality and Relational Operators
  // 3. Type Test Operators
  // 4. Bitwise and Shift Operators

  // 1. Arithmetic Operators

  // 1.1. Addition
  int a = 10;
  int b = 20;
  int c = a + b;

  print("Addition: $c");

  // 1.2. Subtraction

  int d = 20;

  int e = d - a;

  print("Subtraction: $e");

  // 1.3. Multiplication

  int f = a * b;

  print("Multiplication: $f");

  // 1.4. Division

  double g = b / a;

  print("Division: $g");

  // 1.5. Division (Integer)

  int h = b ~/ a;

  print("Division (Integer): $h");

  // 1.6. Modulus

  int i = b % a;

  print("Modulus: $i");

  // 2. Equality and Relational Operators

  // 2.1. Equal

  bool j = a == b;

  print("Equal: $j");

  // 2.2. Not Equal

  bool k = a != b;

  print("Not Equal: $k");

  // 3. Type Test Operators

  // 3.1. is

  bool l = a is int;

  print("is: $l");

  // 3.2. is!

  bool m = a is! int;

  print("is!: $m");
}
